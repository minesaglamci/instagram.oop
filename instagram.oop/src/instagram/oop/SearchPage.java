package instagram.oop;

public class SearchPage {
	private String search;
	private String[] images;
	private String[] videos;
	private String[] reels;
	
	public SearchPage() {
		super();
	}

	public SearchPage(String search, String[] images, String[] videos, String[] reels) {
		super();
		this.search = search;
		this.images = images;
		this.videos = videos;
		this.reels = reels;
	}
	
	

}
